	long parse62Digit(char ch) throws ClassCastException {
		long res = 0l;
		if (ch >= '0' && ch <= '9')
			res = ch - '0';
		else if (ch >= 'a' && ch <= 'z')
			res = ch - 'a' + 10;
		else if (ch >= 'A' && ch <= 'Z')
			res = ch - 'A' + 36;
		else
			throw new ClassCastException("parse62Digit error:" + ch);

		return res;
	}

	long parse62Base(String str) throws ClassCastException {
		long res = 0l;
		for (int i = 0; i < str.length(); ++i) {
			try {
				res = res * 62 + parse62Digit(str.charAt(i));
			} catch (ClassCastException e) {
				throw e;
			}
		}
		return res;
	}

